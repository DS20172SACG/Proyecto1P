package Usuarios;

/**
 *
 * @author Usuario
 */
public class Usuario {
    private String id;
    private String usuario;

    public Usuario() {
    }

    public Usuario(String id, String usuario) {
        this.id = id;
        this.usuario = usuario;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }
    
    
}
